public class TiposDeDatosPrimitivos {
    public static void main(String[] args) {
        byte valorMinimo = Byte.MIN_VALUE;
        byte valorMaximo = Byte.MAX_VALUE;

        System.out.println("Valor mínimo de byte: " + valorMinimo + " y valor máximo de byte: " + valorMaximo);

        short valorMinimoShort = Short.MIN_VALUE;
        short valorMaximoShort = Short.MAX_VALUE;

        System.out.println("Valor mínimo de short: " + valorMinimoShort + " y valor máximo de short: " + valorMaximoShort);

        int valorMinimoInt = Integer.MIN_VALUE;
        int valorMaximoInt = Integer.MAX_VALUE;

        System.out.println("Valor mínimo de int: " + valorMinimoInt + " y valor máximo de int: " + valorMaximoInt);

        long valorMinimoLong = Long.MIN_VALUE;
        long valorMaximoLong = Long.MAX_VALUE;

        System.out.println("Valor mínimo de long: " + valorMinimoLong + " y valor máximo de long: " + valorMaximoLong);

        float valorMinimoFloat = Float.MIN_VALUE;
        float valorMaximoFloat = Float.MAX_VALUE;

        System.out.println("Valor mínimo de float: " + valorMinimoFloat + " y valor máximo de float: " + valorMaximoFloat);

        double valorMinimoDouble = Double.MIN_VALUE;
        double valorMaximoDouble = Double.MAX_VALUE;

        System.out.println("Valor mínimo de double: " + valorMinimoDouble + " y valor máximo de double: " + valorMaximoDouble);

        char valorMinimoChar = Character.MIN_VALUE;
        char valorMaximoChar = Character.MAX_VALUE;

        System.out.println("Valor mínimo de char: " + valorMinimoChar + " y valor máximo de char: " + valorMaximoChar);

        boolean valorMinimoBoolean = Boolean.FALSE;
        boolean valorMaximoBoolean = Boolean.TRUE;

        System.out.println("Valor mínimo de boolean: " + valorMinimoBoolean + " y valor máximo de boolean: " + valorMaximoBoolean);
    }
}
